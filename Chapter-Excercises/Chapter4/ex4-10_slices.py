football_teams = ['Falcons', 'Ravens', 'Packers', 'Colts', 'Panthers', 'Eagles', 'Saints']

# Prints the first three teams using 'slice'
print(f"The first three items are: {football_teams[:3]}")
print()
# Prints the three teams in the middle
print(f"The three teams in the middle of the list are: {football_teams[2:5]}")
print()
# Print the last three items
print(f"The last three items in the list are: {football_teams[4:]}")