# Create list of places to visit
places_to_visit = ['Tokyo', 'New York', 'California', 'Texas', 'Chicago']


# Print list in original order
print(places_to_visit)
print()
# Print list in alphabetical order
print(sorted(places_to_visit))
print()
# Print list to confirm that it's still in the original order
print(places_to_visit)
print()
# Print list in reverse-alphabetical order
print(sorted(places_to_visit, reverse=True))
print()
# Print list to confirm that it's still in the original order
print(places_to_visit)
print()
# Reverse the order of the list
places_to_visit.reverse()
print(places_to_visit)
print()
# Reverse the order of the list again
places_to_visit.reverse()
print(places_to_visit)
print()
# Print list so it's stored in alphabetical order
places_to_visit.sort()
print(places_to_visit)
print()
# Print list so it's stored in reverse-alphabetical order
places_to_visit.sort(reverse=True)
print(places_to_visit)